							<h3>Nuestra<span> Historia.</span></h3>
							<p>
                                Nuestro proyecto surgió de la falta de una institución bilingüe en la ciudad de Metapán.
                                Como primer paso, se crea la asociación DIARVA, quien se convierte en propietaria del Colegio Bill Gates y este a su vez en escuela de verano.
                                DIARVA es también American Eagles Academy y ésta a su vez Escuela de Inglés, Escuela de Ballet y Escuela de Taekwando.
                                En Enero de 2010, el colegio Bill Gates inicia sus labores en su primer año lectivo con seis secciones: <i>Maternal, Kinder 4, Kinder 5, Preparatoria, Primer y Segundo Grado</i>, con una población estudiantil de 44 alumnos.<br>
								Para el año 2011, el número de secciones incrementó, ya que Maternal se divide en Toddler 2 y Toddler 3. Además se incorporó 3er grado. La población estudiantil aumentó a 78. <br>
								En el año 2012 se incrementó cuarto grado; para el 2013 se inicia con quinto grado.
								En agosto de 2014, un grupo de padres y madres comprometidos y visionarios se une y nace el comité de apoyo de padres y madres a raíz de las necesidades de la institución; con su gestión en noviembre de ese mismo año, se pudo realizar la construcción de 2 aulas de parvularia; un proyecto de remodelación que en noviembre de 2015 es finalizado, con un total de 4 aulas y 4 baños, completando así el espacio para contar en nuestra institución desde maternal hasta 9° grado.
                            </p>		
