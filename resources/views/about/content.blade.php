		<div class="col-md-8 services-grid-right">
		<h3>Nuestra <span> Mascota</span></h3>
			<p>
            	La mascota de nuestro colegio se encuentra simbolizada por el águila, símbolo nacional de estados unidos, ave de linaje divina, es considerada el monarca de las aves, simboliza el poder, sabiduría, inspiración de civilizaciones enteras, vive en la pureza de las montañas, alejada de los vicios mundanos, cuida de la familia, el nido y sus dominios.
				<br><b>EL NIDO:</b> El águila busca las alturas, fabrica el nido en las copas de los árboles de gran tamaño o en rocas de difícil acceso Y hace su nido permanente.
				<br><b>EL VUELO:</b> Puede alcanzar una velocidad de 350 kilómetros por hora causando un impacto como de una bala, su capacidad de elevarse en las alturas y luego descender en fulminante picada.
				<br><b>LA VISION:</b> Visión binocular y monocular La mirada aguileña siempre se expande hacia la lejanía. Desde una gran altura el águila puede localizar su presa. Su visión es telescópica su vista es extremadamente aguda que les permite visualizar potenciales presas a distancia,
				<br><b>EL IMPETU GUERRERO:</b> Simplemente usa la tormenta para levantarse más alto puede volar de frente al sol sin enceguecerse. Desde su nido a gran altura vigila su territorio Mientras que la tormenta este destrozando abajo, el águila vuela por encima de ella. El águila no se escapa de la tormenta.
				<br><b>LA RENOVACION:</b> El águila suele experimentar un proceso de cambio, cuando comienza a salirle nuevas plumas y bota el plumaje viejo. Ella toma la difícil y dolorosa decisión de renovarse o morir. El proceso de renovación dura cerca de 150 días, para poder vivir 30 años más.
				<br><b>LA FUERZA:</b> La fuerza de las águilas, que les posibilita alzar en vuelo a presas bastante más pesadas que ellas
			</p>		
